# Привести строку к единому регистру (например, нижнему), чтобы регистр букв не влиял на проверку.

# Удалить из строки все небуквенно-цифровые символы (пробелы, знаки препинания и т. д.), оставив только буквы и цифры.

# Сравнить исходную очищенную строку с её перевёрнутой версией:

# Если они совпадают, строка — палиндром.

# Если нет, строка не является палиндромом.

# Возврат ответа в виде строки "True" или "False"

import re

def is_palindrome(s: str) -> bool:
    # Приводим строку к нижнему регистру и удаляем всё, кроме букв и цифр
    cleaned = re.sub(r'[^a-zа-яё0-9]', '', s.lower())
    # Сравниваем строку с её перевёрнутой версией
    return cleaned == cleaned[::-1]

print(is_palindrome("А роза упала на лапу Азора"))  # True
print(is_palindrome("racecar"))                     # True
print(is_palindrome("hello"))                       # False
print(is_palindrome("12321"))                       # True